def додавання(x, y):
    return x + y

def віднімання(x, y):
    return x - y

def множення(x, y):
    return x * y

def ділення(x, y):
    if y == 0:
        return "Помилка: ділення на нуль"
    return x / y

while True:
    print("Оберіть операцію:")
    print("1. Додавання")
    print("2. Віднімання")
    print("3. Множення")
    print("4. Ділення")
    print("5. Вихід")

    операція = input("Виберіть операцію (1/2/3/4/5): ")

    if операція == '5':
        print("До побачення!")
        break

    if операція not in ('1', '2', '3', '4'):
        print("Некоректний вибір операції")
        continue

    число1 = float(input("Введіть перше число: "))
    число2 = float(input("Введіть друге число: "))

    if операція == '1':
        print("Результат:", додавання(число1, число2))
    elif операція == '2':
        print("Результат:", віднімання(число1, число2))
    elif операція == '3':
        print("Результат:", множення(число1, число2))
    elif операція == '4':
        print("Результат:", ділення(число1, число2))
